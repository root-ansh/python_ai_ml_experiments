CREATE DATABASE IF NOT EXISTS EDULAB;
USE EDULAB;

CREATE TABLE IF NOT EXISTS `DATA_ANALYST_NCR` (
  `JOB_ID` int(11) NOT NULL AUTO_INCREMENT,
  `JOB_TITLE` varchar(255) DEFAULT NULL,
  `EXPERIENCE_REQUIRED` varchar(255) DEFAULT NULL,
  `COMPANY_NAME` varchar(255) DEFAULT NULL,
  `LINK` varchar(255) DEFAULT NULL,
  `KEYSKILLS` varchar(255) DEFAULT NULL,
  `JOB_DESCRIPTION` varchar(255) DEFAULT NULL,
  `SALARY` varchar(255) DEFAULT NULL,
  `DATE_POSTED` varchar(255) DEFAULT NULL,
  `LAST_UPDATED_ON` datetime DEFAULT NOW(),
  PRIMARY KEY (`JOB_ID`)
);

ALTER TABLE `DATA_ANALYST_NCR` AUTO_INCREMENT=0;

CREATE TABLE IF NOT EXISTS `LOCATION_JOBS` (
  `LOCN_ID` int(11) NOT NULL AUTO_INCREMENT,
  `JOB_ID` int(11) NOT NULL,
  `LOCATION` varchar(255) DEFAULT NULL,
  PRIMARY KEY (`LOCN_ID`,`JOB_ID`),
  
  KEY `MY_JOIN` (`JOB_ID`),
  CONSTRAINT `MY_JOIN` 
  FOREIGN KEY (`JOB_ID`) 
  REFERENCES `DATA_ANALYST_NCR` (`JOB_ID`)
) 
